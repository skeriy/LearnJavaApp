package learnapp.pojo;

public class Progress {
    private int theme;
    private int subTheme;
    private int theory;
    private int practic;

    public Progress() {

    }

    public int getTheme() {
        return theme;
    }

    public void setTheme(int theme) {
        this.theme = theme;
    }

    public int getSubTheme() {
        return subTheme;
    }

    public void setSubTheme(int subTheme) {
        this.subTheme = subTheme;
    }

    public int getTheory() {
        return theory;
    }

    public void setTheory(int theory) {
        this.theory = theory;
    }

    public int getPractic() {
        return practic;
    }

    public void setPractic(int practic) {
        this.practic = practic;
    }
}
